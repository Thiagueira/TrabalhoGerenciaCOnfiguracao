    private void jButtonIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIncluirActionPerformed
            
        int quantidade = Integer.parseInt(jTextQuantidadeLivro.getText());
        
        if(quantidade != 0)
        {
        
            Item it = new Item(livro);
            it.setQuantidade(quantidade);
            it.calcularValorItem();
            
            modelo.addRow(new Object[]{livro.getCodigo(),livro.getTitulo(),quantidade,livro.getValor(),it.getValorItem()});
            venda.inserirItem(it);
            soma = soma + it.getValorItem();
            jLabelValorTotalLivro.setText(Float.toString(soma));
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Favor preencher o quantidade do Livro!", "Atenção!", JOptionPane.ERROR_MESSAGE);
                jTextQuantidadeLivro.requestFocus();
        }
        
      
    }//GEN-LAST:event_jButtonIncluirActionPerformed

    private void jButtonRemoverItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoverItemActionPerformed
        
        
         int linha = jTable1.getSelectedRow(); // pega a linha selecionada
                
        
         int codigo = (Integer)jTable1.getModel().getValueAt(linha,0);  //pega a linha (jtable.getselectedRow)
         
         float valor = (Float)jTable1.getModel().getValueAt(linha,4); // primiero parametro é linha e segundo é celula
         
         soma = soma - valor;
         jLabelValorTotalLivro.setText(Float.toString(soma)) ;
        
         venda.removerItem(codigo);
         
         modelo.removeRow(linha);
         
        
    }//GEN-LAST:event_jButtonRemoverItemActionPerformed

    private void limparTabela()
    {
    
        for(int i = jTable1.getRowCount() -1; i>=0; i--)
        {
            modelo.removeRow(i);
        }
               
    }
    
       
    
