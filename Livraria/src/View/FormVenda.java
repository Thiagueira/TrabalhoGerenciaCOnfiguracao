    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
       String cpf = jFormattedTextFieldCPF.getText();
            cliente = FormPrincipal.daoCliente.buscarCliente(cpf);
            if (cliente != null)
            {
                jLabelNomeCliente.setText(cliente.getNome());    //  coloca a informacao no campo selecionado
                jLabelTelefoneCliente.setText(cliente.getTelefone());
                jLabelEmailCliente.setText(cliente.getEmail());
                jLabelEnderecoCliente.setText(cliente.getEndereco().getLogradouro());// pegando endereço e jogando pra um campo só
           
                jButtonConfirmar.setEnabled(true);
                
            }
            else
                JOptionPane.showMessageDialog(null, "Cliente não encontrado!");
                jFormattedTextFieldCPF.requestFocus();
    }//GEN-LAST:event_jButtonBuscarActionPerformed

    private void jButtonBuscarItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarItemActionPerformed
       
        if(jTextCodigoLivro.getText().trim().length() != 0) //verifica-se se o codigo foi digitado
            {    
                    int codigo = Integer.parseInt(jTextCodigoLivro.getText());
                    livro = FormPrincipal.daoLivro.buscarLivro(codigo); //busco o livro do codigo digitado
                    if(livro != null)
                    {
                        
                        jLabelTituloLivro.setText(livro.getTitulo());
                        jLabelEditoraLivro.setText(livro.getFornecedor());
                        jLabelValorLivro.setText(Float.toString(livro.getValor())); // passando float pra string
                        
                    }
                    else
                        JOptionPane.showMessageDialog(null, "Livro não encontrado!", "Atenção!", JOptionPane.ERROR_MESSAGE);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Favor preencher o Código do Livro!", "Atenção!", JOptionPane.ERROR_MESSAGE);
                jTextCodigoLivro.requestFocus();
            }
    }//GEN-LAST:event_jButtonBuscarItemActionPerformed

    
        
    
