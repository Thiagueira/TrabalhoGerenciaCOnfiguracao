    private void limparTabela()
    {
    
        for(int i = jTable1.getRowCount() -1; i>=0; i--)
        {
            modelo.removeRow(i);
        }
               
    }
    
    private void inserirTabela(Livro livro)
    {
        
        modelo.addRow(new Object[]{livro.getCodigo(),livro.getTitulo(),
        livro.getFornecedor(),livro.getEstoque(),livro.getValor(),livro.getData()} );
        
    }
    
    private Livro getLivro()
    {
    
        int linha = jTable1.getSelectedRow(); // pega a linha selecionada
        Livro livro = new Livro(); // criando dados da linha
        
        livro.setCodigo( (Integer)jTable1.getModel().getValueAt(linha, 0) );  //pega a linha (jtable.getselectedRow)
        livro.setTitulo( (String)jTable1.getModel().getValueAt(linha, 1) );
        livro.setFornecedor( (String)jTable1.getModel().getValueAt(linha, 2) );
        livro.setEstoque( (Integer)jTable1.getModel().getValueAt(linha, 3) );
        livro.setValor( (Float)jTable1.getModel().getValueAt(linha, 4) );
        livro.setData( (String)jTable1.getModel().getValueAt(linha, 5) );
        
        
        
        return livro;
        
    }
    
    
    
    
    
    
