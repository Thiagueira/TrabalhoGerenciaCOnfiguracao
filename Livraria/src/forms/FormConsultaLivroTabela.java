    private void btBsucarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBsucarActionPerformed
        limparTabela(); // limpando a tabela || Metodo criado lá em baixo
        if(ckTodos.isSelected()) // verifico se a opção de 'todos' está setada
        {            
            List<Livro> cad = new ArrayList<Livro>(); 
            cad = FormPrincipal.daoLivro.todosLivros(); //pego a lista de todos no dao
            for(Livro livro: cad) // varro a lista
            {
                inserirTabela(livro); // adicionando objeto livro do " for "            
            }
        }
        else // cada a busca seja or um objeto unico
        {            
            if(tfCodigo.getText().trim().length() != 0) //verifica-se se o codigo foi digitado
            {    
                    int codigo = Integer.parseInt(tfCodigo.getText());
                    Livro livro = FormPrincipal.daoLivro.buscarLivro(codigo); //busco o livro do codigo digitado
                    if(livro != null)
                    {
                        inserirTabela(livro); // mostrar informacoes
                    }
                    else
                        JOptionPane.showMessageDialog(null, "Livro não encontrado!", "Atenção!", JOptionPane.ERROR_MESSAGE);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Favor preencher o Código do Livro!", "Atenção!", JOptionPane.ERROR_MESSAGE);
                tfCodigo.requestFocus();
            }
        }
    }//GEN-LAST:event_btBsucarActionPerformed

